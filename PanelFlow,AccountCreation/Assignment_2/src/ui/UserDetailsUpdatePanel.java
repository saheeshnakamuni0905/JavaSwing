    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        AdminOperationsPanel admin = new AdminOperationsPanel(personList, loginList, passwordList);
        DefaultTableModel model= admin.getTable();
        String inputNEUId = txtUserOPNEUIDU.getText();  
        for (int i = 0; i < model.getRowCount(); i++) {
            String neuId = model.getValueAt(i, 0).toString();
            if (neuId.equals(inputNEUId)) {
                userDetailsRowCount = i;
                break;
            }
        }
        
        PersonDetails selectedNEUID = (PersonDetails) model.getValueAt(userDetailsRowCount, 0);  
        selectedNEUID.setFirstName(txtUserOpFirstNameU.getText());
        selectedNEUID.setLastName(txtUserOpLastNameU.getText());
        selectedNEUID.setGender(txtUserOpGenderU.getText());
        selectedNEUID.setDateOfBirth(txtUserOpDOBU.getText());
        
        JOptionPane.showMessageDialog(this, "Details Updated");
    }//GEN-LAST:event_btnUpdateActionPerformed

