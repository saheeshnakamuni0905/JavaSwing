    private void btnSaveUserDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveUserDetailsActionPerformed
        // TODO add your handling code here:
        AdminOperationsPanel admin = new AdminOperationsPanel(personList, loginList, passwordList);
        DefaultTableModel model= admin.getTable();
        
        String inputNEUId = txtUserOPNEUIDU.getText();        
        for (int i = 0; i < model.getRowCount(); i++) {
            String neuId = model.getValueAt(i, 0).toString();
            if (neuId.equals(inputNEUId)) {
                userDetailsRowCount = i;
                break;
            }
        }
        
        LoginCredentials selectedLogin = (LoginCredentials) model.getValueAt(userDetailsRowCount, 3);
        selectedLogin.setUserName(txtUserOpUsernameU.getText());
        
        Password selectedPassword = (Password)model.getValueAt(userDetailsRowCount, 4);
        boolean isAlreadyUsed = false;
        char[] inputPasswordChars = txtUserOpPasswordU.getPassword();
        String inputNewInput = new String(inputPasswordChars);     
        String hashPassword = HashingPassword.hashPassword(inputNewInput);
        
        for(Password word : passwordList.getPasswordList()){
            if(hashPassword.equals(word.getHashPassword())){
                isAlreadyUsed = true;
                break; 
            } 
        }
        if(isAlreadyUsed){
            JOptionPane.showMessageDialog(this, "You cannot choose a previously used password.");
        } else { 
            selectedPassword.setHashPassword(hashPassword);
            selectedPassword.setPassword(inputNewInput);
            JOptionPane.showMessageDialog(this, "Password Updated");
            model.setValueAt(selectedPassword.getPassword(), userDetailsRowCount, 4);   
        }
    }//GEN-LAST:event_btnSaveUserDetailsActionPerformed


