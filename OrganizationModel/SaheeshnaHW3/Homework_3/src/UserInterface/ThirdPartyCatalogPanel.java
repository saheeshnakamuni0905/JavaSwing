    private void populateAuthTable() {
    DefaultTableModel model = (DefaultTableModel) tblAuthority.getModel();
    model.setRowCount(0);

    for (int i = 0; i < thirdPartyList.getThirdPartyList().size(); i++){
        ThirdParty t = thirdPartyList.getThirdPartyList().get(i);
        
        Object[] row = new Object[1];
        row[0] = t; 
        
        model.addRow(row);
    }
   }
