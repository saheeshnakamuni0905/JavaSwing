    private void btnAddAuthActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAuthActionPerformed
        // TODO add your handling code here:
        String thirdParty = txtAuth.getText();
        ThirdParty tp = thirdPartyList.addThirdParty();
        
        tp.setAuthorityName(thirdParty);
        populateAuthTable();
              
        JOptionPane.showMessageDialog(this, "Third Party added");
        
    }//GEN-LAST:event_btnAddAuthActionPerformed

    private void btnDeleteAuthActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAuthActionPerformed
        // TODO add your handling code here: 
        int selectedRowIndex = tblAuthority.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblAuthority.getModel();
        ThirdParty tp = (ThirdParty) model.getValueAt(selectedRowIndex, 0);
        thirdPartyList.deleteThirdParty(tp);
        
        JOptionPane.showMessageDialog(this, "Third Party deleted.");
        populateAuthTable();
    }//GEN-LAST:event_btnDeleteAuthActionPerformed


