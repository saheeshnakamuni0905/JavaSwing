     private void populateStudTable() {
    DefaultTableModel model = (DefaultTableModel) tblStud.getModel();
    model.setRowCount(0);

    for (int i = 0; i < studentList.getStudentList().size(); i++){
        //Person person1 = personList.getPersonList().get(i);
        Student s = studentList.getStudentList().get(i);
        StudentLogin sl = studentLoginList.getStudentLoginList().get(i);
        
        Object[] row = new Object[5];
        row[0] = s; // Assuming getProfId is a method
        row[1] = sl;
        row[2] = sl.getStudentPassword();
        row[3] = s.getStudentName();
        row[4] = s.getStudentEmail();

        model.addRow(row);
    }
}
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblStud.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblStud.getModel();
        Student selectedStudent = (Student) model.getValueAt(selectedRowIndex, 0);
        StudentLogin selectedStudentLogin = (StudentLogin) model.getValueAt(selectedRowIndex, 1);
        studentList.deleteStudent(selectedStudent);
        JOptionPane.showMessageDialog(this, "Student deleted.");
        studentLoginList.deleteStudentLogin(selectedStudentLogin);
        populateStudTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

