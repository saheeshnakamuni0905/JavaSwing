    private void btnAddStudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStudActionPerformed
        // TODO add your handling code here:
         String studentID = txtStudentId.getText();
         String studentName = txtStudentName.getText();
         String studentEmail = txtStudentEmail.getText();
         String studentUsername = txtStudentUserName.getText();
         String studentPassword = txtStudentPassword.getText();
         Student stud= studentList.addNewStudent();
         StudentLogin studlogin= studentLoginList.addNewStudentLogin();
         stud.setStudentName(studentName);
         stud.setStudentID(studentID);
         stud.setStudentEmail(studentEmail);
         studlogin.setStudentUsername(studentUsername);
         studlogin.setStudentPassword(studentPassword);
         
        populateStudTable();
              
        JOptionPane.showMessageDialog(this, "Student added");
        
    }//GEN-LAST:event_btnAddStudActionPerformed

