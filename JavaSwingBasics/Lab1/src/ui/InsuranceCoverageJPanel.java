    private void btnInsuranceCoverageSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsuranceCoverageSaveActionPerformed
        // TODO add your handling code here:
        insuranceCoverage.setInsuranceCompany(txtInsuranceCompany.getText());
        insuranceCoverage.setCorporateID(txtCorporateID.getText());
        insuranceCoverage.setInsuranceAddress(txtCompanyAddress.getText());
        String premiumText = txtPremium.getText();
        if(!premiumText.isEmpty()){
            double premiumAmount = Double.parseDouble(txtPremium.getText());
            insuranceCoverage.setPremiumAmount(premiumAmount);
        }
        else{
            insuranceCoverage.setPremiumAmount(0.0);
        }
       
        String deductibleText = txtDeductible.getText();
        if(!deductibleText.isEmpty()){
            double deductibleAmount = Double.parseDouble(txtDeductible.getText());
            insuranceCoverage.setDeductibleAmount(deductibleAmount); 
        }
        else{
            insuranceCoverage.setDeductibleAmount(0.0); 
        }
        String benefitsDescription = txtAreaBenefitsDesc.getText();
        insuranceCoverage.setBenefitsDescription(benefitsDescription);
        
        JOptionPane.showMessageDialog(this, "Insurance Coverage Details Saved");
        
    }//GEN-LAST:event_btnInsuranceCoverageSaveActionPerformed


